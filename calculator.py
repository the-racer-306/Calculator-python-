input_a = input("First Number: ")

operation = input("enter the operation: ")

input_b = input("Second Number: ")


if operation == "+":
    print(int(input_a) + int(input_b) )
elif operation == "-":
    print(int(input_a) - int(input_b) )
elif operation == "/":
    print(int(input_a) / int(input_b) )
elif operation == "*":
    print(int(input_a) * int(input_b) )